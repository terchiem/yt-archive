  <h2>About YT Archive</h2>
  <p>
    YouTube first started in 2005 and quickly became the most renowned video-sharing website when Google bought it on November 2006 for US$1.65 billion. Since then, the service has grown to 1.9 billion users worldwide with over a billion hours worth of content watched every day. 
  </p>
  <p>
    <a href="https://github.com/terchiem/yt-archive">YT Archive</a> is a project that provides a way to look back at YouTube's beginnings. What kind of videos were watched/uploaded on the platform's first year? What did people vlog about in 2005? Have cat videos always been this popular?
  </p>

  <p>
    This tool searches for videos published in 2005 using the YouTube Data API and saves the results in a database.
  </p>

